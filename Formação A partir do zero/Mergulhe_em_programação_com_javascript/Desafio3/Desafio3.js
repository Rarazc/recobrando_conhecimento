//Parte 1
alert('Parte 1: contador até 10 ');
let contador = 1;
while (contador <= 10) {
    alert(contador);
    contador++;
}

//Parte 2
alert('Parte 2: contador de 10 até 0 ');
let contador2 = 10;
while (contador2 >= 0) {
  alert(contador2);
    contador2--;
}

//Parte 3
alert('Parte 3: contagem regressiva ');
let numero = Number(prompt('Digite um numero para contagem regressiva'));
while (numero >= 0) {
  alert(numero);
    numero--;
}

//Parte 4
alert('Parte 3: contagem progressiva ');
let numero2 = Number(prompt('Digite um numero para contagem progressiva'));
let contador3 = 0;
while (contador3 <= numero2) {
  alert(contador3);
  contador3++;
}